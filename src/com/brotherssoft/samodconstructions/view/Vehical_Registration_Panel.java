    private void loadYearCombo() {
        
        ArrayList<String> years = new ArrayList<>();
        for (int i = 1950; i <= Calendar.getInstance().get(Calendar.YEAR); i++) {
            years.add(i+"");
        }
        cmb_vehicle_year.removeAll();
        for (String year : years) {
            cmb_vehicle_year.addItem(year);
        }
        
    }

